#include<stdio.h>

int main(){
	int n;
	
	scanf("%d", &n);
	
	int fact = 1;
	
	int i = 1;
	
	// while循环计算阶乘（当前被注释掉）
	// while循环的格式：while (条件) { 循环体 }
	// 当条件为真时，重复执行循环体
	// while (i <= n){
	//     // 计算阶乘：将当前的fact与i相乘，并更新fact的值
	//     fact *= i;
	//     // 增加计数器i
	//     i++;
	// }
	
// 使用for循环计算阶乘
	// for循环的格式：for (初始化; 条件; 更新)
	// 初始化：i=1，设置循环计数器的初始值
	// 条件：i<=n，当i小于等于n时，继续循环
	// 更新：i++，每次循环结束后，i增加1
	for (i=1; i<=n; i++){
		// 计算阶乘：将当前的fact与i相乘，并更新fact的值
		fact *= i;
	}
	
	// 输出结果，显示n的阶乘
	// %d用于输出整数，第一个%d替换为n，第二个%d替换为fact
	printf_s("%d!=%d\n", n, fact);
	return 0;
}

/*
关于while循环和for循环计算阶乘的比较：

1. 功能等价性：
这两种循环在本例中都能正确计算阶乘，最终结果相同。

2. 代码简洁性：
for循环更加简洁，将初始化、条件判断和更新操作集中在一行，使代码更加紧凑。
while循环相对分散，需要在循环外初始化变量，在循环体内更新计数器。

3. 可读性：
对于计数型循环（如本例），for循环通常更易读，因为它的结构直观地表达了"对一系列数进行操作"的意图。
while循环在处理不确定迭代次数的情况时可能更自然。

4. 灵活性：
while循环在某些情况下可能更灵活，特别是当循环的终止条件比简单计数更复杂时。
for循环更适合预先知道循环次数或者有明确的递增/递减模式的情况。

5. 性能：
在现代编译器中，这两种循环的性能差异通常可以忽略不计。选择哪种主要取决于代码的可读性和具体需求。

总的来说，在这个计算阶乘的例子中，for循环可能是更好的选择，因为它更简洁且直观地表达了从1到n的迭代意图。
但在其他情况下，选择使用for还是while应该根据具体问题和个人编程风格来决定。
*/
